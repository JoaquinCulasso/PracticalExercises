package calculadora;

/**
 *
 * @author Joaquin
 */
public class Operaciones {

    public static int sumar(int a, int b) {
        return a + b;
    }

    public static int resta(int a, int b) {
        return a - b;
    }

    public static int division(int a, int b) {
        return a / b;
    }

    public static int multiplicacion(int a, int b) {
        return a * b;
    }
}
